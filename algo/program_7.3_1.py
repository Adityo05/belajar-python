# variabel list 
list_buah = ['nanas', 'kelapa', 'rambutan', 'kedondong', 'manggis', 'matoa']
list_angka = [34, 23, 22, 1, 5, 7, 108, 95, 77, 24]

# menampilkan list angka dan buah

print("list buah : ", list_buah)
print("list angka : ", list_angka)

print("\n")
# menampilkan list maksimum dan minimum pada list buah 
print("list buah : ", list_buah)
print("nilai maksimum dari list buah adalah : ",  max(list_buah))
print("nilai minimal dari list buah adalah : ",  min(list_buah))

print("\n")
# menampilkan list maksimum dan minimum pada list angka
print("list angka : ", list_angka)
print("nilai maksimum dari list angka adalah : ",  max(list_angka))
print("nilai minimal dari list angka adalah : ",  min(list_angka))